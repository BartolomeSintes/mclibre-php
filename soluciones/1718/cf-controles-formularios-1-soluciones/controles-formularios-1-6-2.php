<?php
function recoge($var)
{
    $tmp = (isset($_REQUEST[$var]))
        ? trim(htmlspecialchars($_REQUEST[$var], ENT_QUOTES, "UTF-8"))
        : "";
    return $tmp;
}

$fondo = recoge("fondo");
$letra = recoge("letra");

if ($fondo == "" && $letra == "") {
    print "  <p>Usted no quiere cambiar nada</p>\n";
} else {
    print "  <p>Usted quiere cambiar: $fondo";
    if ($fondo != "" && $letra != "") {
        print " y ";
    }
    print "$letra</p>\n";
}
